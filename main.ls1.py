def is_palindrome(s: str) -> bool:
    # 1. Приводим строку к нижнему регистру, чтобы не учитывать разницу между заглавными и строчными буквами.
    s = s.lower()

    # 2. Убираем все пробелы и неалфавитные символы (если требуется учитывать только буквы и цифры).
    s = ''.join(filter(str.isalnum, s))

    # 3. Проверяем, совпадает ли строка с самой собой в обратном порядке.
    return s == s[::-1]


# Примеры использования
print(is_palindrome("А роза упала на лапу Азора"))  # True
print(is_palindrome("hello"))  # False
print(is_palindrome("12321"))  # True
